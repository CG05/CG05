print("{} {} {}".format(123, "문자", 1.234));
print("{:7} {:7} {:7}".format(123, "문자", 1.234));
# 숫자는 오른쪽 정렬, 문자는 왼쪽 정렬이 기본값
print("{:<7} {:<7} {:<7}".format(123, "문자", 1.234)); # 왼쪽 정렬
print("{:>7} {:>7} {:>7}".format(123, "문자", 1.234)); # 오른쪽 정렬
print("{:^7} {:^7} {:^7}".format(123, "문자", 1.234)); # 가운데 정렬

print("{:.3f}".format(123.4577));